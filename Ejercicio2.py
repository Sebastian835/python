#Ingresar la longitud y el ancho de un rectángulo y encontrar su perímetro.

longitud = float(input("Ingrese la longitud del rectangulo: "))         #ingreso de datos
ancho = float(input("Ingrese el ancho del rectangulo: "))

perimetro = 2*(longitud + ancho)             #operacion aritmetica

print("El perimetro del retangulo es ",perimetro)              #mostrar resultado al usuario

